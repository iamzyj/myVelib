package myVelib;

public class Coordinates {
	private double x,y;

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}

	public Coordinates(double x, double y) {
		super();
		this.x = x;
		this.y = y;
	}
	
	
	
}
